function generateBeverage() {
  var result = {
    base:{
      volume:0,
      volumeText:'',
      roast:'',
      bean:'',
      brew:'',
    },
    adds:[/*
      {
        volume:0,
        volumeText:'',
        name:' Default'
      }
      */
    ],
    top:{
      name:''
    },
    title: ''
  };
  let vol = randomIndex(3)+1;
  result.base.volume = vol;
  result.base.volumeText = randomItem(data.drinks.volumes[vol]);
  result.base.roast = randomItem(data.drinks.roasts);
  result.base.bean = randomItem(data.drinks.beans);
  result.base.brew = randomItem(data.drinks.brews);
  //pick 1-3 additives, and then 1 topping, both from the same list of additives
  for (var i=0; i < randomIndex(3)+1; i++) {
    result.adds.push(
      {
        volume:0,
        volumeText:'',
        name: randomItem(data.drinks.additives)
      }
    )
  }
  result.top.name = randomItem(data.drinks.additives);
  let title = randomItem(data.drinks.adjectives) + ' ' + randomItem(data.drinks.nouns); //todo, get more creative with coffee names
  result.title = title.toUpperCase();
  return result;
}

function randomItem(array) {
  return array[randomIndex(array.length)];
}

function generatePerson() {
  var result = '';
  result = randomItem(data.people.first) + ' ' + randomItem(data.people.last);
  return result;
}

function generateCompany() {
  var result = '';
  result = 'The ' + randomItem(data.company.adjectives) + ' ' + randomItem(data.company.nouns);
  return result;
}

function randomIndex(length) {
  var index = Math.floor(Math.random() * length);
  return index;
}
